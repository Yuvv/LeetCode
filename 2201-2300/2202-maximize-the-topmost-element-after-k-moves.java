public class Solution {

    public int maxBefore(int[] nums, int end) {
        int max = -1;
        for (int i = 0; i < end; i++) {
            max = Math.max(max, nums[i]);
        }
        return max;
    }

    public int maximumTop(int[] nums, int k) {
        if (k == 0) {
            return nums[0];
        }
        if (nums.length == 1) {
            if (k % 2 == 0) {
                return nums[0];
            } else {
                // odd operations always result in empty
                return -1;
            }
        }
        if (k > nums.length) {
            return maxBefore(nums, nums.length);
        } else if (k == nums.length) {
            return maxBefore(nums, nums.length - 1);
        } else {
            int maxK_1 = maxBefore(nums, k - 1);
            return Math.max(maxK_1, nums[k]);
        }
    }

    public static void main(String[] args) {
        Solution s = new Solution();
        // 5
        System.out.println(s.maximumTop(new int[] {5,2,2,4,0,6}, 4));
        // -1
        System.out.println(s.maximumTop(new int[] {2}, 1));
    }
}

